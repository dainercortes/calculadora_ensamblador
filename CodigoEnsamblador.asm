INCLUDE 'EMU8086.INC'

.MODEL SMALL 

.STACK 100H  
.DATA        
   
     
VAR1 DW ?
VAR2 DW ?
MENU DB ?
RESL DW ?

                                          
MSGNOMBRE DB 10,13,10,13, "Autor: Yiscarine Yara Rios $"
MSGCEAD DB 10,13, "CEAD Pitalito $"
MSGFECHA DB 10,13, "12/12/2021 $"                                              
MSGM DB 10,13,10,13, "MENU: $"
MSGUNO DB 10,13,10,13, "Opcion 1 = Datos del autor $"
MSGDOS DB 10,13, "Opcion 2 = Suma $"
MSGTRES DB 10,13, "Opcion 3 = Resta $"  
MSGCUATRO DB 10,13, "Opcion 4 = Multiplicacion $"
MSGCINCO DB 10,13, "Opcion 5 = Division $"
MSGSEIS DB 10,13, "Opcion 6 = Numero positivo o negativo $"
MSGSIETE DB 10,13, "Opcion 7 = Salir $"       
MSGOP DB 10,13,10,13, "Eleccion: $"
MSGNUM DB 10,13,10,13, "Numero: $"
MSGRESL DB 10,13,10,13, "Resultado de la operacion: $"  
MSGP DB 10,13,10,13, "El numero ingresado es positivo $"
MSGN DB 10,13,10,13, "El numero ingresado es negativo $" 
MSGR DB 10,13,10,13, "Digite 1 para continuar o 2 para salir $" 

                                                
.CODE        

MOV AX,@DATA 
MOV DS, AX   

INICIAR:     
MOV AH, 09H
MOV DX, OFFSET MSGM
INT 21H    

  
MOV AH, 09H
MOV DX, OFFSET MSGUNO
INT 21H 

MOV AH, 09H
MOV DX, OFFSET MSGDOS
INT 21H 

MOV AH, 09H
MOV DX, OFFSET MSGTRES
INT 21H 

MOV AH, 09H
MOV DX, OFFSET MSGCUATRO
INT 21H 

MOV AH, 09H
MOV DX, OFFSET MSGCINCO
INT 21H 

MOV AH, 09H
MOV DX, OFFSET MSGSEIS
INT 21H  

MOV AH, 09H
MOV DX, OFFSET MSGSIETE
INT 21H   


 

MOV AH, 09H
MOV DX, OFFSET MSGOP
INT 21H 

MOV AH,01H
INT 21H
SUB AL,30H
MOV MENU,AL 

CMP MENU,7
JGE SALIR      

CMP MENU,6
JE SIGNO 

CMP MENU,1 
JL SALIR
JE DATOS



MOV AH, 09H
MOV DX, OFFSET MSGNUM
INT 21H               
CALL SCAN_NUM              
MOV VAR1,CX
MOV AH, 09H
MOV DX, OFFSET MSGNUM
INT 21H 
CALL SCAN_NUM
MOV VAR2,CX 

CMP MENU,5
JE DIVISION 

CMP MENU,4
JE MULTIPLICACION  
   
CMP MENU,3
JE RESTA
 
CMP MENU,2
JE SUMA  
                                                    
DATOS:
MOV AH, 09H
MOV DX, OFFSET MSGNOMBRE
INT 21H 
MOV AH, 09H
MOV DX, OFFSET MSGCEAD
INT 21H 
MOV AH, 09H
MOV DX, OFFSET MSGFECHA
INT 21H 
JMP REINICIAR

SUMA:     
MOV AH,09H
MOV DX,OFFSET MSGRESL
INT 21H  

MOV AX,VAR1
ADD AX,VAR2
MOV RESL,AX
CALL PRINT_NUM 

JMP REINICIAR


RESTA: 
MOV AH,09H
MOV DX,OFFSET MSGRESL
INT 21H   
   
MOV AX,VAR1
SUB AX,VAR2
MOV RESL, AX
CALL PRINT_NUM

JMP REINICIAR     


MULTIPLICACION:
MOV AH,09H
MOV DX,OFFSET MSGRESL
INT 21H
      
MOV AX,VAR1
MUL VAR2
MOV RESL, AX
CALL PRINT_NUM 

JMP REINICIAR     


DIVISION:      
MOV AH,09H
MOV DX,OFFSET MSGRESL
INT 21H  

XOR DX,DX 
MOV AX,VAR1
MOV BX,VAR2
DIV BX  
MOV RESL,BX
CALL PRINT_NUM 

JMP REINICIAR
      
      
SIGNO:
MOV AH, 09H
MOV DX, OFFSET MSGNUM
INT 21H
               
CALL SCAN_NUM              
MOV VAR1,CX

CMP CX,0

JG POSITIVO
JL NEGATIVO


POSITIVO: 
MOV AH, 09H
MOV DX, OFFSET MSGP
INT 21H

JMP REINICIAR
 
NEGATIVO:
MOV AH, 09H
MOV DX, OFFSET MSGN
INT 21H

JMP REINICIAR 
        
        
REINICIAR:  
MOV AH, 09H
MOV DX, OFFSET MSGR
INT 21H   

MOV AH,01H
INT 21H
SUB AL,30H
MOV MENU,AL 

CMP AL,1 
 
JE INICIAR  
   
   
SALIR:  
MOV AH,07H   
INT 21H

MOV AH, 4CH  
INT 21H        

 
DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS   

END           
